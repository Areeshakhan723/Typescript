console.log("num1 * num2 =", num1 * num2); // Multiplication

console.log("num1 / num2 =", num1 / num2); // Division

console.log("num1 % num2 =", num1 % num2); // Modulus

console.log("num1 ** num2 =", num1 ** num2); // Exponentiation

